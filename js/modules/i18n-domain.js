  const LETTER = /[A-Za-zÀ-žĄąĆćĘęŁłŃńÓóŚśŹźŻż0-9]/;
  let compiled = null;   // { lang, exact:Map, subKeys:[...] }
  let applying = false;
  let scheduled = false;

  function lang() {
    try { return (typeof currentLanguage !== 'undefined') ? currentLanguage : (window.currentLanguage || 'pl'); }
    catch (e) { return 'pl'; }
  }

  function compile(l) {
    const src = DICT[l];
    if (!src) { compiled = { lang: l, exact: null, subKeys: [] }; return; }
    const exact = new Map();
    const subKeys = [];
    Object.keys(src).forEach(k => {
      exact.set(k, src[k]);
      if (k.length >= 6) subKeys.push(k);       // krótkie tylko przez dopasowanie dokładne
    });
    subKeys.sort((a, b) => b.length - a.length); // najdłuższe najpierw
    compiled = { lang: l, exact, subKeys, src };
  }

  // podmiana podłańcuchów z kontrolą granic (znak przed/po nie może być literą/cyfrą)
  function subReplace(text, key, val) {
    let out = '', i = 0;
    while (true) {
      const p = text.indexOf(key, i);
      if (p === -1) { out += text.slice(i); break; }
      const before = p > 0 ? text[p - 1] : '';
      const after = (p + key.length < text.length) ? text[p + key.length] : '';
      // granica słowa sprawdzana tylko, gdy krawędź klucza sama jest literą
      const bOk = !LETTER.test(key[0]) || !before || !LETTER.test(before);
      const aOk = !LETTER.test(key[key.length - 1]) || !after || !LETTER.test(after);
      if (bOk && aOk) { out += text.slice(i, p) + val; i = p + key.length; }
      else { out += text.slice(i, p + key.length); i = p + key.length; }
    }
    return out;
  }

  function translateString(s) {
    if (!s || !compiled || !compiled.exact) return s;
    const trimmed = s.trim();
    if (!trimmed) return s;
    const hit = compiled.exact.get(trimmed);
    if (hit !== undefined) {
      // zachowaj białe znaki wiodące/końcowe
      const li = s.indexOf(trimmed);
      return s.slice(0, li) + hit + s.slice(li + trimmed.length);
    }
    // pass podłańcuchowy — tylko gdy tekst wygląda na polski (heurystyka)
    if (!/[ąćęłńóśźżĄĆĘŁŃÓŚŹŻ]/.test(s) &&
        !/\b(Brak|Usu[nń]|Dodaj|Zapisz|Edytuj|Anuluj|Wybierz|Okres|Odczyt|Metoda|Klient|Obiekt|Raport|Suma|Netto|Zamknij|Podaj|Kliknij|klient|obiekt|okres|zapisz|analiz|protok|regres|zuż|Zuż)/.test(s)) return s;
    let out = s;
    for (const k of compiled.subKeys) {
      if (out.indexOf(k) !== -1) out = subReplace(out, k, compiled.src[k]);
    }
    return out;
  }

  const ATTRS = ['placeholder', 'title'];

  function walk(root) {
    if (!root) return;
    const tw = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode: function (n) {
        const p = n.parentNode && n.parentNode.nodeName;
        if (p === 'SCRIPT' || p === 'STYLE') return NodeFilter.FILTER_REJECT;
        return n.nodeValue && n.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
      }
    });
    let n;
    while ((n = tw.nextNode())) {
      const t = translateString(n.nodeValue);
      if (t !== n.nodeValue) n.nodeValue = t;
    }
    if (root.querySelectorAll) {
      root.querySelectorAll('[placeholder],[title],input[type=button],input[type=submit]').forEach(el => {
        ATTRS.forEach(a => {
          const v = el.getAttribute && el.getAttribute(a);
          if (v) { const t = translateString(v); if (t !== v) el.setAttribute(a, t); }
        });
        if (el.tagName === 'INPUT' && (el.type === 'button' || el.type === 'submit') && el.value) {
          const t = translateString(el.value); if (t !== el.value) el.value = t;
        }
      });
    }
  }

  function apply() {
    const l = lang();
    if (!DICT[l]) return;                    // pl / inne języki — nic nie robimy
    if (!compiled || compiled.lang !== l) compile(l);
    applying = true;
    try { walk(document.body); } finally { applying = false; }
  }

  function schedule() {
    if (scheduled || applying) return;
    scheduled = true;
    setTimeout(function () { scheduled = false; apply(); }, 30);
  }

  // Observer na cały body — łapie rendery modułów, modale, podglądy raportów.
  function start() {
    const mo = new MutationObserver(function (muts) {
      if (applying) return;
      if (!DICT[lang()]) return;
      for (const m of muts) {
        if (m.type === 'childList' && m.addedNodes.length) { schedule(); return; }
      }
    });
    mo.observe(document.body, { childList: true, subtree: true });

    // opakowanie alert/confirm — komunikaty systemowe modułów
    const _alert = window.alert.bind(window);
    const _confirm = window.confirm.bind(window);
    window.alert = function (msg) { return _alert(DICT[lang()] ? translateString(String(msg)) : msg); };
    window.confirm = function (msg) { return _confirm(DICT[lang()] ? translateString(String(msg)) : msg); };

    // przełączenie języka (setLanguage w index.html wywołuje renderDashboard →
    // observer i tak zadziała, ale dla pewności tłumaczymy też od razu)
    const _setLang = window.setLanguage;
    if (typeof _setLang === 'function') {
      window.setLanguage = function (l) { _setLang(l); schedule(); };
    }
    // tłumaczenie napisów rysowanych na wykresach (canvas) — bez zmian w kodzie rysującym
    if (window.CanvasRenderingContext2D) {
      const proto = CanvasRenderingContext2D.prototype;
      ['fillText', 'strokeText'].forEach(function (fn) {
        const orig = proto[fn];
        proto[fn] = function (text) {
          if (DICT[lang()] && typeof text === 'string') arguments[0] = translateString(text);
          return orig.apply(this, arguments);
        };
      });
    }

    apply();
  }

  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', start);
  else start();

  window.DomainI18n = { apply: apply, dict: DICT, translate: translateString };
